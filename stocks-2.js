// Тестовый случай 1. Ожидаемая прибыль 7.
// (5-1) + (6-3) = 4 + 3 = 7
let prices1 = [7, 1, 5, 3, 6, 4]; // 7

// Тестовый случай 2. Цены только падают, прибыли нет.
let prices2 = [7, 6, 4, 3, 1]; // 0

// Функция для вычисления максимальной прибыли при многократных сделках.
let maxProfit = function (prices) {
    // Инициализируем итоговую прибыль.
    let result = 0;

    // Проходим по массиву, начиная со второго элемента.
    for (let i = 1; i < prices.length; i++) {
        // Если цена сегодня выше, чем вчера...
        if (prices[i] > prices[i - 1]) {
            // ...то мы можем получить прибыль. Добавляем разницу к общему результату.
            // Это эквивалентно "покупке вчера и продаже сегодня".
            result += prices[i] - prices[i - 1];
        }
    }

    // Возвращаем накопленную прибыль.
    return result;
};

// Выводим результаты для проверки.
console.log(maxProfit(prices1));
console.log(maxProfit(prices2));
